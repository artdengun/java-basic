package com.deni.javadasar.dasar;

public class OperatorAritmatika {
    public static void main(String[] args) {
        // operator aritmatika dalah operator dasar
        int a = 40;
        int b = 30;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a/b);
        System.out.println(a%b);
        System.out.println(++a);
        System.out.println(--a);
   }
}

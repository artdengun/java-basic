package com.deni.javadasar.dasar;

public class    PerulanganDoWhile {
    public static void main(String[] args) {
        // while melakukan pengecekan di depan
        // do while melakukan pengecekan didepan
        int i = 4;
        do{
            System.out.println("Hello " + i);
            i++;
        }while (i<=10);
    }
}

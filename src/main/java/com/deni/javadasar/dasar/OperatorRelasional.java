package com.deni.javadasar.dasar;

public class OperatorRelasional {
    public static void main(String[] args) {
        // operator Relasional adalah membandingkan nilai
        int a = 30;
        int b = 20;

        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
    }
}
